#!/usr/bin/env node
// deterministic node identities + per-pair jwt secrets -> .env / jwt/
import { createECDH, randomBytes } from 'node:crypto';
import { writeFileSync, mkdirSync } from 'node:fs';

function keypair() {
  const priv = randomBytes(32).toString('hex');
  const e = createECDH('secp256k1');
  e.setPrivateKey(Buffer.from(priv, 'hex'));
  const pub = e.getPublicKey('hex', 'uncompressed').slice(2); // enode id
  return { priv, pub };
}

const besu = keypair();
const geth = keypair();

// lighthouse fixed libp2p identity -> teku dials it with retries (deterministic topology)
const b58alphabet = '123456789ABCDEFGHJKLMNPQRSTUVWXYZabcdefghijkmnopqrstuvwxyz';
function base58(buf) {
  let n = BigInt('0x' + buf.toString('hex'));
  let out = '';
  while (n > 0n) { out = b58alphabet[Number(n % 58n)] + out; n /= 58n; }
  for (const b of buf) { if (b === 0) out = '1' + out; else break; }
  return out;
}
const lh = keypair();
const lhE = createECDH('secp256k1');
lhE.setPrivateKey(Buffer.from(lh.priv, 'hex'));
const lhPub = lhE.getPublicKey(null, 'compressed');
const pb = Buffer.concat([Buffer.from([0x08, 0x02, 0x12, 0x21]), lhPub]);
const mh = Buffer.concat([Buffer.from([0x00, pb.length]), pb]);
const lhPeerId = base58(mh);

mkdirSync('jwt', { recursive: true });
writeFileSync('jwt/pair-a.hex', randomBytes(32).toString('hex')); // besu <-> teku
writeFileSync('jwt/pair-b.hex', randomBytes(32).toString('hex')); // geth <-> lighthouse
writeFileSync('config/besu-node.key', besu.priv);
writeFileSync('config/lighthouse-node.key', Buffer.from(lh.priv, 'hex'));

const env = `# generated by scripts/gen-identities.mjs
COMPOSE_FILE=docker-compose.yml:../monitoring/docker-compose.yml
NET_NAME=eth-devnet-network
NET_EXTERNAL=false
GETH_IMAGE=ethereum/client-go:v1.17.4
BESU_IMAGE=hyperledger/besu:26.6.1
TEKU_IMAGE=consensys/teku:26.7.0
LIGHTHOUSE_IMAGE=sigp/lighthouse:v8.2.0
PRYSMCTL_IMAGE=gcr.io/offchainlabs/prysm/cmd/prysmctl:stable
PRYSM_VALIDATOR_IMAGE=gcr.io/offchainlabs/prysm/validator:stable
PROMETHEUS_IMAGE=prom/prometheus:v3.1.0
GRAFANA_IMAGE=grafana/grafana:11.4.0
PROMETHEUS_PORT=9091
GRAFANA_PORT=3001
GETH_NODEKEY=${geth.priv}
BESU_ENODE=enode://${besu.pub}@besu:30303
LIGHTHOUSE_MULTIADDR=/dns4/lighthouse/tcp/9000/p2p/${lhPeerId}
`;
writeFileSync('.env', env);
console.log(env);
